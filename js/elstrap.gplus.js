window.___gcfg = {
  lang: Drupal.settings.elstrapGooglePlus.lang
};

(function() {
  var po = document.createElement('script');
  po.type = 'text/javascript';
  po.async = true;
  po.src = 'https://apis.google.com/js/plusone.js?publisherid=' + Drupal.settings.elstrapGooglePlus.publisherId;
  var s = document.getElementsByTagName('script')[0];
  s.parentNode.insertBefore(po, s);
})();
